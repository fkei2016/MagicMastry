using System.Collections.Generic;
using UnityEngine;
public class SubjectSCR : MonoBehaviour
{
    /*********************************************************************************************************************************/
    // 変数.
    /*********************************************************************************************************************************/
    // ObseverのListを作成する.
    protected List<ObseverSCR> m_obseverList = new List<ObseverSCR>(); // private
    /*********************************************************************************************************************************/
    // 追加.
    /*********************************************************************************************************************************/
    /**
     * @brief ObserverをListに追加する
     * @param 追加するオブザーバー
     * @author shinji
     * @date 09/16
     */
    public void Attach(ObseverSCR obsever)
    {
        m_obseverList.Add(obsever);
    }
    /*********************************************************************************************************************************/
    // 削除.
    /*********************************************************************************************************************************/
    /**
     * @brief Observerをlistから削除する
     * @param 削除するオブザーバー
     * @author shinji
     * @date 09/16
     */
    public void Deteach(ObseverSCR obsever)
    {
        m_obseverList.Remove(obsever);
    }
    /*********************************************************************************************************************************/
    // 通知.
    /*********************************************************************************************************************************/
    /**
     * @brief Observerに通知を送る
     * @author shinji
     * @date 09/16
     */
    public virtual void Notify()
    {
       foreach(ObseverSCR obsever in m_obseverList)
            obsever.Accept();
    }
    /*********************************************************************************************************************************/
}